int find_path() {
  int left = 0, right = 0;
  queue[0].x = 0;
  queue[0].y = 0;
  queue[0].move = 0;
  queue[0].pre = NONE;

  while (1) {
    for (size_t choice = 0; choice < 4; ++choice) {
      int new_x = queue[left].x + x_move[choice];
      int new_y = queue[left].y + y_move[choice];

      if ((new_x < LINE_SIZE) && (new_x >= 0) && (new_y < COLUMN_SIZE) &&
          (new_y >= 0) && (maze[new_x][new_y] == '0')) {
        queue[++right].move = move[choice];
        queue[right].x = new_x;
        queue[right].y = new_y;
        queue[right].pre = left;
        maze[new_x][new_y] = VISITED;
        // print_maze();

        if (is_end(right)) {
          return right;
        }
      }
    }
    ++left;
  }
}

void print_path(int end) {
  if (queue[end].pre == NONE) {
    return;
  }
  print_path(queue[end].pre);
  printf("%c", queue[end].move);
}

